    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente atualizar este Mestre?", "Atualizar Mestre", JOptionPane.YES_NO_CANCEL_OPTION);
        if (opcao == JOptionPane.YES_OPTION) {
            try {
                Mestre p = new Mestre();
                p.setId(Integer.parseInt(textoCodigo.getText()));
                p.setNome(textoNome.getText());
                MestreDAO dao = new MestreDAO();
                dao.atualizar(p);
                textoCodigo.setEnabled(true);
                idJogadores.setEnabled(true);
                botaoConsultar.setEnabled(true);
                textoNome.setEnabled(false);
                botaoAtualizar.setEnabled(false);
                textoCodigo.setText("");
                textoNome.setText("");
                ChangeOptions();
                JOptionPane.showMessageDialog(this, "Mestre atualizado com sucesso", "Atualizar Mestre", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Atualizar Mestre", JOptionPane.ERROR_MESSAGE);
            }
        }
        botaoAtualizar.transferFocus();
    }//GEN-LAST:event_botaoAtualizarActionPerformed

    private void botaoConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoConsultarActionPerformed
        try {
            MestreDAO dao = new MestreDAO();
            Mestre p = dao.obterPorNome(idJogadores.getSelectedItem().toString()).get(0);
            textoNome.setText(p.getNome());
            textoCodigo.setText(Integer.toString(p.getId()));
            textoCodigo.setEnabled(false);
            idJogadores.setEnabled(false);
            botaoConsultar.setEnabled(false);
            textoNome.setEnabled(true);
            botaoAtualizar.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Consultar Mestre", JOptionPane.ERROR_MESSAGE);
        }
        botaoConsultar.transferFocus();
    }//GEN-LAST:event_botaoConsultarActionPerformed

